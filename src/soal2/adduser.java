    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtus.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Inputkan Username!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (txtpw.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Inputkan Password!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String us = txtus.getText();
        String pw = String.valueOf(txtpw.getPassword());

        try {
            Connection conn = config.configDB();
            String cekSql = "SELECT * FROM tb_user WHERE username=?";
            PreparedStatement cekStmt = conn.prepareStatement(cekSql);
            cekStmt.setString(1, us);
            java.sql.ResultSet rs = cekStmt.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Username sudah digunakan!");
                rs.close();
                cekStmt.close();
                conn.close();
                return;
            }

            rs.close();
            cekStmt.close();

            int konfirmasi = JOptionPane.showConfirmDialog(
                    this,
                    "Username: " + us
                    + "\nPassword: " + pw
                    + "\nApakah data sudah benar?",
                    "Konfirmasi",
                    JOptionPane.YES_NO_OPTION
            );

            if (konfirmasi == JOptionPane.YES_OPTION) {

                String sql = "INSERT INTO tb_user (username, password, role) VALUES (?, ?, 'operator')";
                PreparedStatement stmt = conn.prepareStatement(sql);

                stmt.setString(1, us);
                stmt.setString(2, pw);

                stmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");

                txtus.setText("");
                txtpw.setText("");

                stmt.close();
            } else {
                JOptionPane.showMessageDialog(this, "Silakan periksa kembali data!");
            }

            conn.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal simpan: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

